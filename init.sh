# "Basic" packages
sudo pacman -Syu --noconfirm neovim git fzf ripgrep zsh starship exa which python3 python-pip bat tmux direnv zoxide jq gcc chafa unzip playerctl tr cut grep command

curr_dir=$(pwd)
git clone https://aur.archlinux.org/yay.git ~/yay
cd ~/yay
makepkg -si
cd $curr_dir
yay -S ruby-build rbenv --noconfirm

curl -o- https://raw.githubusercontent.com/nvm-sh/nvm/v0.39.1/install.sh | bash
export NVM_DIR="$([ -z "${XDG_CONFIG_HOME-}" ] && printf %s "${HOME}/.nvm" || printf %s "${XDG_CONFIG_HOME}/nvm")"
[ -s "$NVM_DIR/nvm.sh" ] && \. "$NVM_DIR/nvm.sh" # This loads nvm
nvm install 16

# Config neovim
mkdir ~/.config
git clone https://github.com/mluna711/shinobu.git ~/.config/nvim
git clone --depth 1 https://github.com/wbthomason/packer.nvim ~/.local/share/nvim/site/pack/packer/start/packer.nvim
pip3 install neovim
nvim --headless -c 'autocmd User PackerComplete quitall' -c 'PackerSync'
nvim --headless -c 'autocmd VimEnter * quitall' -c 'echo "Help.\n"'

# Config zsh
sudo chsh -s $(which zsh) vagrant
sh -c "$(curl -fsSL https://raw.githubusercontent.com/ohmyzsh/ohmyzsh/master/tools/install.sh)" "" --unattended &> /dev/null
cp ~/.config/nvim/.zshrc ~
cp ~/.config/nvim/starship.toml ~/.config

# Config tmux
git clone https://github.com/tmux-plugins/tpm ~/.tmux/plugins/tpm
cp ~/.config/nvim/.tmux.conf ~

# Config alacritty
cp -r ~/.config/nvim/alacritty ~/.config

